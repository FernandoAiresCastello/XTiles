	void drawRect( SDL_Surface* surface, int x, int y, int w, int h, int rgb )
	{
		x *= w;
		y *= h;

		SDL_Rect dstrect;
		dstrect.x = x;
		dstrect.y = y;
		dstrect.w = w;
		dstrect.h = h;

		SDL_FillRect(surface, &dstrect, rgb);
	}

	void putPixel( SDL_Surface* surface, Sint16 x, Sint16 y, Uint32 rgb )
	{
		if ( x < 0 || x >= surface->w || y < 0 || y >= surface->h )
			return;

		int bpp;
		Uint8 *p;

		bpp = surface->format->BytesPerPixel;
		p = (Uint8 *) surface->pixels + y * surface->pitch + x * bpp;
		switch (bpp) {
		case 1:
			*p = rgb;
			break;
		case 2:
			*(Uint16 *) p = rgb;
			break;
		case 3:
			if (SDL_BYTEORDER == SDL_BIG_ENDIAN) {
				p[0] = (rgb >> 16) & 0xff;
				p[1] = (rgb >> 8) & 0xff;
				p[2] = rgb & 0xff;
			} else {
				p[0] = rgb & 0xff;
				p[1] = (rgb >> 8) & 0xff;
				p[2] = (rgb >> 16) & 0xff;
			}
			break;
		case 4:
			*(Uint32 *) p = rgb;
			break;
		}
	}

	void drawTile( SDL_Surface* surface, int tileIndex,
				   int fgColor, int bgColor, int x, int y, int w, int h, bool align )
	{
		drawTile(surface, tileset.tile[tileIndex], fgColor, bgColor, x, y, w, h, align);
	}

	void drawTile( SDL_Surface* surface, Tile tile,
				   int fgColor, int bgColor, int x, int y, int w, int h, bool align)
	{
		register int i = 0;

		if ( align )
		{
			x *= TILE_WIDTH;
			y *= TILE_HEIGHT;
		}

		for ( register int py = y; py < y + TILE_HEIGHT; py++ )		
		for ( register int px = x; px < x + TILE_WIDTH; px++ )
		{
			char pixcode = tile.pixel[i++];

			if ( pixcode == '0' )
				drawRect( surface, px, py, w, h, palette.color[bgColor] );
			else if ( pixcode == '1' )
				drawRect( surface, px, py, w, h, palette.color[fgColor] );
			else
				continue;
		}
	}

	void drawTileMC( int colorMode, SDL_Surface* s,	int tileIndex,
		int x, int y, int w, int h, bool align )
	{
		Tile tile = tileset.tile[tileIndex];
		register int i = 0;

		if ( align )
		{
			x *= TILE_WIDTH;
			y *= TILE_HEIGHT;
		}

		for ( register int py = y; py < y + TILE_HEIGHT; py++ )		
		for ( register int px = x; px < x + TILE_WIDTH; px++ )
		{
			if ( colorMode == MULTICOLOR_DIRECT )
				drawRect( s, px, py, w, h, tile.pixel[i++] );
			else if ( colorMode == MULTICOLOR_INDEXED )
				drawRect( s, px, py, w, h, palette.color[tile.pixel[i++]] );
			else
				return;
		}
	}

	void drawString( SDL_Surface* surface,
		int x, int y, int fgColor, int bgColor, int w, int h, const char* str )
	{
		int character = 0xff;
		int stringIndex = 0;
		int maxX = surface->w / (TILE_WIDTH * w);
		int maxY = surface->h / (TILE_HEIGHT * h);

		while ( character != '\0' )
		{
			character = str[stringIndex++];
			if ( character == '\0' ) break;
			drawTile( surface, character, fgColor, bgColor, x++, y, w, h );

			if ( x >= maxX )
			{
				x = 0;
				y++;

				if ( y >= maxY )
					break;
			}
		}
	}

	void fill( SDL_Surface* surface,
		int tileIndex, int fgColor, int bgColor, int w, int h )
	{
		int maxX = surface->w / (TILE_WIDTH * w);
		int maxY = surface->h / (TILE_HEIGHT * h);

		for ( int y = 0; y < maxY; y++ )
		for ( int x = 0; x < maxX; x++ )
			drawTile( surface, tileIndex, fgColor, bgColor, x, y, w, h );
	}

	void clear( SDL_Surface* surface, int bgColor )
	{
		SDL_Rect rect;

		rect.x = 0;
		rect.y = 0;
		rect.w = surface->w;
		rect.h = surface->h;

		SDL_FillRect( surface, &rect, color(bgColor) );
		SDL_UpdateRect( surface, 0, 0, rect.w, rect.h );
	}

