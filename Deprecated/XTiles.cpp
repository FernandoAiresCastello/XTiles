	Uint32& color( int paletteIndex )
	{
		return palette.color[paletteIndex];
	}

	Tile& tile( int tilesetIndex )
	{
		return tileset.tile[tilesetIndex];
	}

	void setColor( int paletteIndex, Uint32 rgb )
	{
		palette.color[paletteIndex] = rgb;
	}

	int getColorR( Uint32 rgb )
	{
		return (rgb & 0xff0000) >> 16;
	}

	int getColorG( Uint32 rgb )
	{
		return (rgb & 0x00ff00) >> 8;
	}

	int getColorB( Uint32 rgb )
	{
		return (rgb & 0x0000ff);
	}

	void setColorRGB( Uint32& rgb, int r, int g, int b )
	{
		int color = r;
		color <<= 8;
		color |= g;
		color <<= 8;
		color |= b;
	
		rgb = color;
	}

	void setColorRGB( int paletteIndex, int r, int g, int b )
	{
		setColorRGB(color(paletteIndex), r, g, b);
	}

	void setTile( int tilesetIndex, Tile tile )
	{
		tileset.tile[tilesetIndex] = tile;
	}

	void setTile( Tile& tile, int tilesetIndex )
	{
		tile = tileset.tile[tilesetIndex];
	}

	void setTile( int tilesetIndex,
		const char* line1, const char* line2, const char* line3, const char* line4, 
		const char* line5, const char* line6, const char* line7, const char* line8 )
	{
		Tile tile;

		for ( int i = 0; i < TILE_WIDTH; i++ )
		{
			tile.pixel[ 0+i] = line1[i];
			tile.pixel[ 8+i] = line2[i];
			tile.pixel[16+i] = line3[i];
			tile.pixel[24+i] = line4[i];
			tile.pixel[32+i] = line5[i];
			tile.pixel[40+i] = line6[i];
			tile.pixel[48+i] = line7[i];
			tile.pixel[56+i] = line8[i];
		}

		tileset.tile[tilesetIndex] = tile;
	}

	void setTile( Tile& tile, Tile otherTile )
	{
		for (int i = 0; i < TILE_SIZE; i++)
			tile.pixel[i] = otherTile.pixel[i];
	}

	void setTile( int tileIndex, int copyTileIndex )
	{
		setTile(tile(tileIndex), tile(copyTileIndex));
	}

	void clearTile( Tile& tile )
	{
		for (int i = 0; i < TILE_SIZE; i++)
			tile.pixel[i] = TILE_PIXEL_OFF;
	}

	void clearTile( int tilesetIndex )
	{
		clearTile(tile(tilesetIndex));
	}

	void clearTileset()
	{
		for ( int t = 0; t < TILESET_SIZE; t++ )
		for ( int p = 0; p < TILE_SIZE; p++ )
			tileset.tile[t].pixel[p] = '0';
	}

	void invertTile( Tile& tile )
	{
		for (int i = 0; i < TILE_SIZE; i++)
			tile.pixel[i] = (tile.pixel[i] == TILE_PIXEL_OFF ? TILE_PIXEL_ON : TILE_PIXEL_OFF);
	}

	void invertTile( int tileIndex )
	{
		invertTile(tile(tileIndex));
	}

	void clearPalette()
	{
		for ( int c = 0; c < PALETTE_SIZE; c++ )
			palette.color[c] = 0x000000;
	}

	bool loadTileset( const char* file )
	{
		FILE* fp = fopen( file, "rb" );
		if (!fp) return false;
		fread( tileset.tile, sizeof(Tile), TILESET_SIZE, fp );
		fclose(fp);
		return true;
	}

